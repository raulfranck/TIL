
'''
    Logic operators with Python
'''

years_old = 20
height = 1.80

# AND is a operator like && with javascript
result = years_old >= 30 and height >= 1.80

print(result)

# OR is a operator like || in javascript

result2 = years_old >= 30 or height >= 1.80
print(result2)

# NOT is a operator like ! in javascript

has_money = True

print(not has_money) # Flase